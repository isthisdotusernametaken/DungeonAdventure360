package model;

/**
 * This class constructs the item type for the item that will be
 * used and/or collected in the dungeon adventure game.
 *
 * @author Joshua Barbee, Tinh Diep, Alexander Boudreaux
 * @version 16 December 2022
 */
public enum ItemType {

    /**
     * The abstraction pillar.
     */
    ABSTRACTION,
    /**
     * The encapsulation pillar.
     */
    ENCAPSULATION,
    /**
     * The inheritance pillar.
     */
    INHERITANCE,
    /**
     * The polymorphism pillar.
     */
    POLYMORPHISM,
    /**
     * The plank item.
     */
    PLANKS,
    /**
     * The vision potion item.
     */
    VISION_POTION,
    /**
     * Thea health potion item.
     */
    HEALTH_POTION,
    /**
     * The buff potion item.
     */
    BUFF_POTION
}
